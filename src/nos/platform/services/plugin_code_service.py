PLUGIN_BASE = "nos.plugins"


def _module_path_for_import(module_path: str) -> str:
    """
    Dotted module path for ``importlib.import_module`` / ``Node.load(dev)`` / ``Workflow.load(dev)``.

    Plugin code under ``PLUGIN_PATH`` is imported as a **normal** top-level package (e.g.
    ``my_pkg.node_plugin``). Nothing is rewritten to ``nos.plugins.nodes.*``.
    """
    if not module_path or not isinstance(module_path, str):
        return (module_path or "").strip()
    raw = module_path.strip()
    if raw.startswith("ext:"):
        return raw[4:].strip()
    return raw


def _normalize_plugin_module_path(module_path: str, plugin_type: Optional[str] = None) -> str:
    """
    Normalize **only** for mapping module paths to files under ``PLUGIN_PATH`` (open/save/create).

    - Paths starting with ``nos.`` — unchanged (virtual explorer tree).
    - ``nodes....`` (node) — prefix ``nos.plugins.`` so ``nodes.dev.foo`` → ``nos.plugins.nodes.dev.foo``.
    - ``workflows....`` / ``old....`` (workflow) — prefix ``nos.plugins.``.
    - Anything else — returned as-is (external package dotted path, e.g. ``my_pkg.node_plugin``).

    For **run node dev** / **run workflow dev**, use :func:`_module_path_for_import` instead.
    """
    if not module_path or not isinstance(module_path, str):
        return (module_path or "").strip()
    raw = module_path.strip()
    if raw.startswith("ext:"):
        return raw[4:].strip()
    if raw.startswith("nos."):
        return raw
    if plugin_type == "node" and raw.startswith("nodes."):
        return f"{PLUGIN_BASE}.{raw}"
    if plugin_type == "workflow" and (raw.startswith("workflows.") or raw.startswith("old.")):
        return f"{PLUGIN_BASE}.{raw}"
    return raw


def _resolve_external_plugin_py(module_path: str) -> Tuple[Optional[str], Optional[str]]:
    """
    Map a **non-nos.** dotted path (external pip package under ``PLUGIN_PATH``) to a ``.py`` file.

    Layout: ``<PLUGIN_PATH>/<dist>/src/<pkg>/.../<module>.py`` (same as ``plugin create`` scaffold).
    """
    raw = (module_path or "").strip()
    if raw.startswith("ext:"):
        raw = raw[4:].strip()
    if not raw or raw.startswith("nos."):
        return None, None
    parts = [p for p in raw.split(".") if p]
    if len(parts) < 2:
        return None, None
    try:
        from nos.platform.config.paths import get_plugin_path
        from nos.platform.plugins.scaffold import _parse_slug

        _pkg, dist = _parse_slug(parts[0])
        plugin_root = get_plugin_path().resolve()
    except ValueError:
        return None, None

    base = plugin_root / dist / "src" / _pkg
    if len(parts) == 2:
        py = base / f"{parts[1]}.py"
    else:
        py = base.joinpath(*parts[1:-1]) / f"{parts[-1]}.py"
    try:
        py = py.resolve()
        py.relative_to(plugin_root)
    except ValueError:
        return None, None
    dev_root_real = os.path.realpath(str(plugin_root))
    alt = os.path.realpath(str(py))
    if not alt.startswith(dev_root_real + os.sep) and alt != dev_root_real:
        return None, None
    if not alt.endswith(".py"):
        return None, None
    return alt, dev_root_real


def _get_nos_pkg_dir() -> Optional[str]:
    """Get the nos package directory."""
    try:
        import nos
        return os.path.dirname(os.path.abspath(nos.__file__))
    except Exception:
        return None


def _plugin_dev_root() -> Optional[str]:
    """
    Engine «Plugins explorer» root: ``PLUGIN_PATH`` (see ``nos.platform.config.paths``), default
    ``~/.nos/plugins``. Same logical path as ``nos.platform.api.console.routes._get_plugin_dev_root``
    (no mkdir here).
    """
    try:
        from nos.platform.config.paths import get_plugin_path

        p = str(get_plugin_path().resolve())
    except ValueError:
        return None
    return p if os.path.isdir(p) else None


def resolve_node_path(module_path: str) -> Tuple[Optional[str], Optional[str]]:
    """
    Map ``nos.plugins.<...>`` (virtual tree) **or** an external package path (``pkg.sub.module``)
    to a ``.py`` under ``PLUGIN_PATH``.
    """
    path = _normalize_plugin_module_path(module_path or "", "node")
    parts = path.split(".")
    if not parts or parts[0] != "nos":
        return _resolve_external_plugin_py(module_path or "")

    rel_parts = parts[1:]
    if not rel_parts or rel_parts[0] != "plugins":
        return None, None

    dev_root = _plugin_dev_root()
    if not dev_root or len(rel_parts) < 2:
        return None, None

    sub = [p for p in rel_parts[1:] if p and p not in (".", "..")]
    if not sub:
        return None, None

    dev_root_real = os.path.realpath(dev_root)
    alt = os.path.realpath(os.path.join(dev_root_real, *sub) + ".py")
    if not alt.startswith(dev_root_real + os.sep):
        return None, None

    return alt, dev_root_real


def resolve_workflow_path(module_path: str) -> Tuple[Optional[str], Optional[str]]:
    """
    Resolve ``nos.plugins.workflows...`` / ``nos.plugins.old...`` or generic ``nos.plugins...``
    trees under ``PLUGIN_PATH``, or an external package path (``pkg.workflow_plugin``).
    """
    path = _normalize_plugin_module_path(module_path or "", "workflow")
    parts = path.split(".")
    if not parts or parts[0] != "nos":
        return _resolve_external_plugin_py(module_path or "")

    rel_parts = parts[1:]
    if not rel_parts or rel_parts[0] != "plugins":
        return None, None

    dev_root = _plugin_dev_root()
    if not dev_root or len(rel_parts) < 2:
        return None, None

    sub = rel_parts[1:]
    alt = os.path.realpath(os.path.join(dev_root, *sub) + ".py")
    allowed_wf = os.path.realpath(os.path.join(dev_root, "workflows"))
    allowed_old = os.path.realpath(os.path.join(dev_root, "old"))
    if sub and sub[0] == "workflows" and (alt.startswith(allowed_wf + os.sep) or alt == allowed_wf):
        return alt, allowed_wf
    if sub and sub[0] == "old" and (alt.startswith(allowed_old + os.sep) or alt == allowed_old):
        return alt, allowed_old

    return resolve_node_path(path)


def _ensure_package_init(allowed_dir: str, target_dir: str) -> None:
    """Ensure every directory from allowed_dir to target_dir has __init__.py."""
    allowed_real = os.path.realpath(allowed_dir)
    current = os.path.realpath(target_dir)
    
    if not current.startswith(allowed_real + os.sep) and current != allowed_real:
        return
    
    to_create = []
    while current and current != allowed_real and current != os.path.dirname(current):
        init_path = os.path.join(current, "__init__.py")
        if not os.path.isfile(init_path):
            to_create.append(init_path)
        current = os.path.dirname(current)
    
    for init_path in to_create:
        with open(init_path, "w", encoding="utf-8") as f:
            f.write("")


