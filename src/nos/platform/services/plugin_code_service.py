def read_node_code(
    node_id: str = None,
    module_path: str = None
) -> SaveCodeResult:
    """
    Read node plugin file content directly from file system.
    
    Does NOT require DB registration - works with any file under nodes directory.
    
    Args:
        node_id: Node identifier (used with module_path for context, not for path resolution)
        module_path: Full module path (REQUIRED) - e.g., nos.plugins.nodes.my_folder.my_node
        
    Returns:
        SaveCodeResult with content and file_path
    """
    # module_path is required - no default folder
    if not module_path:
        return SaveCodeResult(
            success=False,
            message="module_path is required (e.g., nos.plugins.nodes.<folder>.<module_name>)"
        )
    
    actual_module_path = module_path
    
    # Resolve to file path
    file_path, _ = resolve_node_path(actual_module_path)
    if not file_path:
        return SaveCodeResult(
            success=False,
            message=f"Invalid module_path: {actual_module_path}"
        )
    
    # Check if file exists
    if not os.path.exists(file_path):
        return SaveCodeResult(
            success=False,
            message=f"File not found: {file_path}"
        )
    
    # Read content
    try:
        with open(file_path, "r", encoding="utf-8") as f:
            content = f.read()
    except OSError as e:
        logger.error(f"read_node_code: read failed: {e}")
        return SaveCodeResult(
            success=False,
            message=f"Read error: {str(e)}"
        )
    
    return SaveCodeResult(
        success=True,
        message=f"Node code loaded from: {file_path}",
        file_path=file_path,
        content=content
    )


