def create_node(
    node_id: str,
    class_name: str,
    module_path: str,
    name: Optional[str] = None
) -> SaveCodeResult:
    """
    Create a new node plugin file with template code.
    
    Args:
        node_id: Unique node identifier
        class_name: Python class name for the node
        module_path: Full or relative path (e.g. nos.plugins.nodes.dev.my_node, nodes.dev.my_node, or dev_.my_node)
        name: Display name (defaults to node_id titlecased)
        
    Returns:
        SaveCodeResult with operation status
    """
    normalized_path = _normalize_plugin_module_path(module_path or "", "node")
    # Validate and resolve path
    file_path, allowed_dir = resolve_node_path(normalized_path)
    if not file_path:
        return SaveCodeResult(
            success=False,
            message="Invalid module_path (must map under plugin dev root, see PLUGIN_PATH / ~/.nos/plugins)",
        )
    
    # Check if file already exists
    if os.path.exists(file_path):
        return SaveCodeResult(
            success=False,
            message=f"File already exists: {file_path}"
        )
    
    # Generate name if not provided
    if not name:
        name = node_id.replace("_", " ").title()
    
    # Generate content from template (use normalized path for reg command)
    content = NODE_TEMPLATE.format(
        node_id=node_id,
        class_name=class_name,
        module_path=normalized_path,
        name=name
    )
    
    # Save the file and create DB record
    return save_node_code(node_id, class_name, normalized_path, content, name=name, is_create=True)


def save_node_code(
    node_id: str,
    class_name: str,
    module_path: str,
    content: str,
    name: Optional[str] = None,
    is_create: bool = False,
) -> SaveCodeResult:
    """
    Save node code to file only (no DB record - use register_node for that).
    
    Args:
        node_id: Node identifier
        class_name: Python class name
        module_path: Full module path
        content: Python source code
        name: Display name (defaults to node_id titlecased)
        
    Returns:
        SaveCodeResult with operation status
    """
    # Resolve path
    file_path, allowed_dir = resolve_node_path(module_path)
    if not file_path:
        return SaveCodeResult(
            success=False,
            message="Invalid module_path (must map under plugin dev root, see PLUGIN_PATH / ~/.nos/plugins)",
        )
    
    # Create directory structure and write file
    try:
        target_dir = os.path.dirname(file_path)
        os.makedirs(target_dir, exist_ok=True)
        if target_dir != allowed_dir:
            _ensure_package_init(allowed_dir, target_dir)
        
        with open(file_path, "w", encoding="utf-8") as f:
            f.write(content)
    except OSError as e:
        logger.error(f"save_node_code: write failed: {e}")
        return SaveCodeResult(
            success=False,
            message=f"Write error: {str(e)}"
        )
    
    verb = "created" if is_create else "saved"
    return SaveCodeResult(
        success=True,
        message=f"Node file {verb}: {file_path}. Use 'reg node {node_id}' to register.",
        file_path=file_path,
        content=content
    )


def write_and_register_node_code(
    node_id: str,
    class_name: str,
    module_path: str,
    content: str,
) -> SaveCodeResult:
    """
    Write node Python source to file, try to register, and update DB registration_status.
    Used by both REST API (save-code) and Socket.IO (save command).
    
    Args:
        node_id: Node identifier
        class_name: Python class name for registration
        module_path: Full module path (e.g. nos.plugins.nodes.folder.my_node)
        content: Python source code to write
        
    Returns:
        SaveCodeResult with success, message, file_path, registration_status, registration_error
    """
    from nos.core.engine.plugin_loader import try_register_node
    from .sqlalchemy.node import repository as node_repo
    from .sqlalchemy.node.model import RegistrationStatus
    
    module_path = _normalize_plugin_module_path(module_path or "", "node")
    file_path, allowed_dir = resolve_node_path(module_path)
    if not file_path:
        return SaveCodeResult(
            success=False,
            message="Invalid module_path (must map under plugin dev root, see PLUGIN_PATH / ~/.nos/plugins)",
        )
    
    try:
        target_dir = os.path.dirname(file_path)
        os.makedirs(target_dir, exist_ok=True)
        if target_dir != allowed_dir:
            _ensure_package_init(allowed_dir, target_dir)
        with open(file_path, "w", encoding="utf-8") as f:
            f.write(content)
    except OSError as e:
        logger.error(f"write_and_register_node_code: write failed: {e}")
        return SaveCodeResult(
            success=False,
            message=f"Write error: {str(e)}"
        )
    
    reg_error = None
    ok = False
    try:
        ok, reg_error = try_register_node(module_path, class_name, node_id)
        status = RegistrationStatus.OK.value if ok else RegistrationStatus.ERROR.value
    except Exception as e:
        logger.error(f"write_and_register_node_code: register failed: {e}")
        status = RegistrationStatus.ERROR.value
        reg_error = str(e)
    
    try:
        node, err = node_repo.update(
            node_id,
            {
                "registration_status": status,
                "registration_date": datetime.utcnow(),
            },
        )
        if err == "not_found":
            logger.debug("write_and_register_node_code: node %s not in DB, status not persisted", node_id)
    except Exception as e:
        logger.error(f"write_and_register_node_code: DB update failed: {e}")
    
    return SaveCodeResult(
        success=True,
        message="File saved",
        file_path=file_path,
        registration_status=status,
        registration_error=reg_error if not ok else None,
    )


def register_node(
    node_id: str,
    class_name: str,
    module_path: str,
    name: Optional[str] = None,
    created_by: str = "console"
) -> SaveCodeResult:
    """
    Register a node (import module, validate class, create/update DB record).
    
    Args:
        node_id: Node identifier
        class_name: Python class name
        module_path: Full module path
        name: Display name (defaults to node_id titlecased)
        created_by: User who is registering the node
        
    Returns:
        SaveCodeResult with registration status
    """
    from nos.core.engine.plugin_loader import try_register_node
    from .sqlalchemy.node import repository as node_repo
    from .sqlalchemy.node.model import RegistrationStatus
    from ..extensions import db
    
    module_path = _normalize_plugin_module_path(module_path or "", "node")
    # Generate display name if not provided
