def create_workflow(
    workflow_id: str,
    class_name: str,
    module_path: str,
    name: Optional[str] = None
) -> SaveCodeResult:
    """
    Create a new workflow plugin file with template code.
    
    Args:
        workflow_id: Unique workflow identifier
        class_name: Python class name for the workflow
        module_path: Full or relative path (e.g. nos.plugins.workflows.dev.my_workflow or workflows.dev.my_workflow)
        name: Display name (defaults to workflow_id titlecased)
        
    Returns:
        SaveCodeResult with operation status
    """
    normalized_path = _normalize_plugin_module_path(module_path or "", "workflow")
    # Validate and resolve path
    file_path, allowed_dir = resolve_workflow_path(normalized_path)
    if not file_path:
        return SaveCodeResult(
            success=False,
            message="Invalid module_path. Must be under nos.plugins.workflows"
        )
    
    # Check if file already exists
    if os.path.exists(file_path):
        return SaveCodeResult(
            success=False,
            message=f"File already exists: {file_path}"
        )
    
    # Generate name if not provided
    if not name:
        name = workflow_id.replace("_", " ").title()
    
    # Generate content from template (use normalized path)
    content = WORKFLOW_TEMPLATE.format(
        workflow_id=workflow_id,
        class_name=class_name,
        module_path=normalized_path,
        name=name
    )
    
    # Save the file and create DB record
    return save_workflow_code(workflow_id, class_name, normalized_path, content, name=name)


def save_workflow_code(
    workflow_id: str,
    class_name: str,
    module_path: str,
    content: str,
    name: Optional[str] = None
) -> SaveCodeResult:
    """
    Save workflow code to file only (no DB record - use register_workflow for that).
    
    Args:
        workflow_id: Workflow identifier
        class_name: Python class name
        module_path: Full module path
        content: Python source code
        name: Display name (defaults to workflow_id titlecased)
        
    Returns:
        SaveCodeResult with operation status
    """
    # Resolve path
    file_path, allowed_dir = resolve_workflow_path(module_path)
    if not file_path:
        return SaveCodeResult(
            success=False,
            message="Invalid module_path. Must be under nos.plugins.workflows"
        )
    
    # Create directory structure and write file
    try:
        target_dir = os.path.dirname(file_path)
        os.makedirs(target_dir, exist_ok=True)
        
        with open(file_path, "w", encoding="utf-8") as f:
            f.write(content)
    except OSError as e:
        logger.error(f"save_workflow_code: write failed: {e}")
        return SaveCodeResult(
            success=False,
            message=f"Write error: {str(e)}"
        )
    
    return SaveCodeResult(
        success=True,
        message=f"Workflow file created: {file_path}. Use 'reg workflow {workflow_id}' to register.",
        file_path=file_path,
        content=content
    )


