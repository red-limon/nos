class SimpleSumNode(Node):
    """Sums a and b from state, writes result to state['sum_result']."""

    def _do_execute(self, state: dict, params_dict: dict) -> NodeOutput:
        a = state.get("a", 0)
        b = state.get("b", 0)
        result = a + b
        state["sum_result"] = result
        return NodeOutput(output=dict(result=result))


class SimpleDotNode(Node):
    """Dot product of vec_a and vec_b from state, writes to state['dot_result']."""

    def _do_execute(self, state: dict, params_dict: dict) -> NodeOutput:
        va = state.get("vec_a", [1, 0])
        vb = state.get("vec_b", [0, 1])
        result = sum(x * y for x, y in zip(va, vb))
        state["dot_result"] = result
        return NodeOutput(output=dict(result=result))


